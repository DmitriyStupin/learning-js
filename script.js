/*
 Правило объявления переменных:
 1. Можно писать как в латинице, так и в кириллице (не рекомедуется!);
 2. Можно использовать "$", "_";
 3. Можно использовать цифры (но в начале переменной нельзя!)

 Переменные как правило пишут в camelCase нотации (каждое слово, кроме первого, начинается с большой буквы)
 Можно и так "BASE_URL" (Обычно используют для констант)
*/